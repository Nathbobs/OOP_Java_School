public class Project2 {
    public static void main(String[] args) {
        int intVal1, intVal2;

        for (intVal1 = 1; intVal1 < 6; intVal1++){
            for (intVal2 = 5; intVal2 >= 1 ; intVal2--){
                System.out.println(intVal1 + " multiplied by "+ intVal2 + " = " + intVal1*intVal2);
            }
            System.out.print("");
        }
        // Use nested loop

    }
}
